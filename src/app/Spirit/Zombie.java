package app.Spirit;

public class Zombie {
    private int HP = 0;

    public int getHP() {
        return HP;
    }

    public void setHP(int hP) {
        HP = hP;
    }

    private int DEF = 0;

    public int getDEF() {
        return DEF;
    }

    public void setDEF(int dEF) {
        DEF = dEF;
    }

    private int ATK = 100 ;

    public int getATK() {
        return ATK;
    }

    public void setATK(int aTK) {
        ATK = aTK;
    }

}
