package app.CardSkillData;

public class CardSkillData {

    private String Name;

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    private int CONT;

    public int getCONT() {
        return CONT;
    }

    public void setCONT(int cONT) {
        CONT = cONT;
    }

    private int ATK;

    public int getATK() {
        return ATK;
    }

    public void setATK(int aTK) {
        ATK = aTK;
    }

    private int DEF;

    public int getDEF() {
        return DEF;
    }

    public void setDEF(int dEF) {
        DEF = dEF;
    }

    @Override
    public String toString() {
        return super.toString();
    }

}
